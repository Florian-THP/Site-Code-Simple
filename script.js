function copierTexte() {
    // Récupérer le texte dans la div
    var texte = document.querySelector('.codecopier').innerText;

    // Créer un élément temporaire pour stocker le texte à copier
    var elementTemporaire = document.createElement('textarea');
    elementTemporaire.value = texte;

    // Ajouter cet élément à la page (hors de vue)
    document.body.appendChild(elementTemporaire);

    // Sélectionner le contenu de l'élément temporaire
    elementTemporaire.select();
    elementTemporaire.setSelectionRange(0, 99999); // Pour les mobiles

    // Exécuter la commande de copie
    document.execCommand('copy');

    // Supprimer l'élément temporaire après copie
    document.body.removeChild(elementTemporaire);

    // Facultatif : afficher un message de succès
    alert("Texte copié !");
}